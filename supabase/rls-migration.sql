insert into storage.buckets (id, name, public)
values ('finance-receipts', 'finance-receipts', true)
on conflict (id) do nothing;

drop policy if exists "Users can upload their own finance receipts" on storage.objects;
drop policy if exists "Users can update their own finance receipts" on storage.objects;
drop policy if exists "Users can delete their own finance receipts" on storage.objects;
drop policy if exists "Public can view finance receipts" on storage.objects;

create policy "Users can upload their own finance receipts"
  on storage.objects
  for insert
  to authenticated
  with check (
    bucket_id = 'finance-receipts'
    and split_part(name, '/', 1) = auth.uid()::text
  );

create policy "Users can update their own finance receipts"
  on storage.objects
  for update
  to authenticated
  using (
    bucket_id = 'finance-receipts'
    and split_part(name, '/', 1) = auth.uid()::text
  )
  with check (
    bucket_id = 'finance-receipts'
    and split_part(name, '/', 1) = auth.uid()::text
  );

create policy "Users can delete their own finance receipts"
  on storage.objects
  for delete
  to authenticated
  using (
    bucket_id = 'finance-receipts'
    and split_part(name, '/', 1) = auth.uid()::text
  );

create policy "Public can view finance receipts"
  on storage.objects
  for select
  to public
  using (bucket_id = 'finance-receipts');

